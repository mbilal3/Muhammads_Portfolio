/* Find the Total Death count and the Leading Cause of Death */
select Gender,Deaths,Major_Cause from Ny_Death_Data
group by Gender,Deaths,Major_Cause;

/* What is the leading cause of death for Hispanic individuals, where the total number of deaths are more than 1500 */
Select Racial_Ethnicity,Deaths,Major_Cause FROM Ny_Death_Data
GROUP BY Racial_Ethnicity,Deaths,Major_Cause
Having Deaths > 1500 And Racial_Ethnicity = 'Hispanic';

/* Leading Cause of Death and death count among Asian and Pacific-Islanders in 2011, where deaths are at least 500 */
Select Racial_Ethnicity,Deaths,Major_Cause,Year
FROM Ny_Death_Data
WHERE Year = '2011'
GROUP BY racial_ethnicity,Deaths,Major_Cause,Year
Having Deaths > 500 And Racial_Ethnicity = 'Asian and Pacific Islander';

/* Find the top 5 Mortality Rate causes in NYC for Black Individuals */

FROM Ny_Death_Data
GROUP BY Racial_Ethnicity,Death_Rate,Major_Cause
Having Racial_Ethnicity = 'Black Non-Hispanic'
ORDER BY Major_Cause
asc FETCH FIRST 5 ROWS ONLY;

